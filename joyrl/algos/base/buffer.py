class BufferType(Enum):
    ''' buffer type enum
    '''
    REPLAY = 1
    REPLAY_QUE = 2
    PER = 3
    PER_QUE = 4
    ONPOLICY = 5
    ONPOLICY_QUE = 6

class BufferCreator:
    ''' buffer creator
    '''
    def __init__(self, algo_cfg):
        self.cfg = algo_cfg
        self.buffer_type = BufferType[algo_cfg.buffer_type]
    def __call__(self):
        if self.buffer_type == BufferType.REPLAY:
            return ReplayBuffer(self.cfg)
        elif self.buffer_type == BufferType.REPLAY_QUE:
            return ReplayBufferQue(self.cfg)
        elif self.buffer_type == BufferType.ONPOLICY_QUE:
            return OnPolicyBufferQue(self.cfg)
        elif self.buffer_type == BufferType.PER_QUE:
            return PrioritizedReplayBufferQue(self.cfg)
        else:
            raise NotImplementedError
            
class ReplayBuffer:
    def __init__(self, cfg: MergedConfig):
        if hasattr(cfg, 'max_buffer_size'): self.capacity = cfg.max_buffer_size
        self.batch_size = cfg.batch_size
        self.buffer = [] 
        self.position = 0 # pointer of buffer
    
    def push(self, exps):
        ''' 
        '''
        for exp in exps:
            if len(self.buffer) < self.capacity:
                self.buffer.append(None)
            self.buffer[self.position] = exp
            self.position = (self.position + 1) % self.capacity

    def sample(self, **kwargs):
        ''' sample a batch of transitions   
        '''
        batch = random.sample(self.buffer, self.batch_size) 
        return batch
    
    def __len__(self):
        ''' return the current size of the buffer
        '''
        return len(self.buffer)

class ReplayBufferQue:
    ''' same as ReplayBuffer, but use deque to store the transitions
    '''
    def __init__(self, cfg: MergedConfig):
        if hasattr(cfg, 'max_buffer_size'): self.capacity = cfg.max_buffer_size
        self.learn_frequecy = 1
        if hasattr(cfg, 'learn_frequecy'): self.learn_frequecy = cfg.learn_frequecy
        self.batch_size = cfg.batch_size
        self.buffer = deque(maxlen=self.capacity)
        self.new_exp_cnt = 0

    def push(self, exps: list):
        ''' push a transition into the buffer
        '''
        for exp in exps:
            self.buffer.append(exp)
        self.new_exp_cnt += len(exps)

    def sample(self, **kwargs):
        ''' sample a batch of transitions
        '''
        sequential = kwargs.get('sequential', False)
        if self.batch_size > len(self.buffer): # if the buffer is not full, return None
            return None
        if self.new_exp_cnt < self.learn_frequecy: # if the buffer is not full, return None
            return None
        self.new_exp_cnt -= self.learn_frequecy
        if sequential: # sequential sampling
            rand = random.randint(0, len(self.buffer) - self.batch_size)
            batch = [self.buffer[i] for i in range(rand, rand + self.batch_size)]
            return batch
        else:
            batch = random.sample(self.buffer, self.batch_size)
            return batch
        
    def clear(self):
        ''' clear the buffer
        '''
        self.buffer.clear()
    def __len__(self):
        ''' return the current size of the buffer
        '''
        return len(self.buffer)

